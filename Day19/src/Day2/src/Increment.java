import java.util.*;
public class Increment {
    public static void main(String[] args) {
        int prevalue=2;
        System.out.println("Original value" +"="+(prevalue));
        System.out.println("post-increment"+"="+ (prevalue++));
        System.out.println("After post-increment"+"="+ (prevalue));
        System.out.println("pre-increment"+"="+ (prevalue++));
        System.out.println("After pre-increment"+"="+ (prevalue++));


    }
}
